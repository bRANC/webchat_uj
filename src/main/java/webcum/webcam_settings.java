    void writename() {
        try (PrintWriter iro = new PrintWriter(new File("dolgok.txt"))) {
            if (!jname.getText().trim().isEmpty()) {
                iro.println(jname.getText().trim() + "\n");
            }
            iro.flush();
            iro.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
